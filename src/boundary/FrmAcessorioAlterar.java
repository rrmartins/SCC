    private void jBCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBCadastrarActionPerformed
        // TODO add your handling code here:
        if (!this.jTFDescricaoAcessorio.getText().isEmpty())
        {
            Vector alteraAcessorio = new Vector();
            alteraAcessorio.addElement(this.jTFDescricaoAcessorio.getText());
            alteraAcessorio.addElement(lin.get(0));


            try
            {
                try {
                    try {
                        this.cadastroAcessorio.alterarAcessorio(alteraAcessorio);
                    } catch (groovyjarjarcommonscli.ParseException ex) {
                        Logger.getLogger(FrmAcessorioAlterar.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    this.jTFDescricaoAcessorio.setText("");
                    this.dispose();
                } catch (ConexaoException ex) {
                    Logger.getLogger(FrmAcessorioAlterar.class.getName()).log(Level.SEVERE, null, ex);
                }
            } catch (SQLException erro)
            {
                JOptionPane.showMessageDialog(null, erro.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(FrmAcessorioAlterar.class.getName()).log(Level.SEVERE, null, ex);
            } catch (MinhaException ex) {
                Logger.getLogger(FrmAcessorioAlterar.class.getName()).log(Level.SEVERE, null, ex);
            }

            
        }else
                JOptionPane.showMessageDialog(null, "É necessário a Descrição do Acessório","Atenção" , JOptionPane.WARNING_MESSAGE);


}//GEN-LAST:event_jBCadastrarActionPerformed

